class Queue:
    def __init__(self):
        self.items = []

    def enqueue(self, item):
        self.items.insert(0, item)

    def dequeue(self):
        if not self.is_empty():
            return self.items.pop()
        else:
            return None

    def is_empty(self):
        return len(self.items) == 0

qu = Queue()
qu.enqueue(1)
qu.enqueue(3)
qu.enqueue(5)
qu.enqueue(7)
